import Foundation

/// AI 직원이 사용할 수 있는 액션 가이드
struct AIActionGuide {
    /// 시스템 프롬프트에 추가할 액션 가이드
    static let guide = """

    ---

    ## 🛠️ 사용 가능한 작업 도구

    당신은 다음 작업을 실제로 수행할 수 있습니다. 필요할 때 아래 포맷을 사용하세요:

    ### 1. 위키 문서 생성
    프로젝트 기획서, 명세서, 회의록 등을 작성할 때 사용하세요.

    ```
    [CREATE_WIKI: 문서 제목 | 카테고리]
    # 문서 내용 (마크다운)

    ## 섹션 1
    내용...

    ## 섹션 2
    내용...
    [/CREATE_WIKI]
    ```

    카테고리: `spec` (명세서), `meeting` (회의록), `guide` (가이드), `reference` (참고자료)

    **예시:**
    ```
    [CREATE_WIKI: 픽셀오피스 기능명세서 v1.0 | spec]
    # 픽셀오피스 기능명세서

    ## 1. 프로젝트 개요
    PixelOffice는 가상 오피스에서 AI 직원들과 협업하는 macOS 앱입니다...

    ## 2. 핵심 기능
    - 프로젝트 관리
    - 직원 관리
    - 칸반 보드
    [/CREATE_WIKI]
    ```

    ### 2. 태스크 생성
    개발, 디자인, 테스트 등 작업 항목을 칸반에 추가할 때 사용하세요.

    ```
    [CREATE_TASK]
    제목: 태스크 제목
    설명: 상세 설명 (무엇을, 왜, 어떻게)
    우선순위: high/medium/low
    예상시간: 8
    태그: UI, 기능, 백엔드
    [/CREATE_TASK]
    ```

    **예시:**
    ```
    [CREATE_TASK]
    제목: 직원 생성 UI 개선
    설명: 부서 선택 후 직군을 멀티 선택할 수 있도록 체크박스 UI 추가. 선택한 직군들을 하단에 요약 표시.
    우선순위: high
    예상시간: 4
    태그: UI, 직원관리
    [/CREATE_TASK]
    ```

    ### 3. 부서/직원 멘션
    다른 부서나 직원에게 작업을 요청하거나 협업을 시작할 때 사용하세요.

    ```
    @부서명 요청 메시지
    @직원명 요청 메시지
    ```

    **예시:**
    ```
    @디자인팀 직원 생성 화면의 UI 디자인 작업을 요청드립니다.
    부서 선택 후 직군을 멀티 선택할 수 있는 인터페이스가 필요합니다.

    @개발팀 백엔드 API 엔드포인트 추가가 필요합니다.
    ```

    ### 4. 협업 기록
    부서 간 회의, 리뷰, 공동 작업 내용을 기록할 때 사용하세요.

    ```
    [COLLABORATION: 협업 제목]
    참여부서: 기획팀, 디자인팀, 개발팀
    내용: 협업 내용 및 논의 사항
    결과: 최종 결정 사항 및 다음 액션 아이템
    [/COLLABORATION]
    ```

    **예시:**
    ```
    [COLLABORATION: 직군 멀티 선택 기능 설계 회의]
    참여부서: 기획팀, 디자인팀, 개발팀
    내용: 직원 추가 시 여러 직군을 선택할 수 있도록 개선. 체크박스 UI로 구현하기로 결정.
    결과: 디자인팀이 목업 작성, 개발팀이 2일 내 구현 예정
    [/COLLABORATION]
    ```

    ---

    ## 📋 작업 흐름 예시

    사용자가 "픽셀오피스 기능 명세서를 작성하고 개발 태스크들을 만들어줘"라고 요청하면:

    1. **위키 문서 생성**
    ```
    [CREATE_WIKI: 픽셀오피스 기능명세서 v1.0 | spec]
    (명세서 내용 작성)
    [/CREATE_WIKI]
    ```

    2. **태스크 생성** (여러 개 가능)
    ```
    [CREATE_TASK]
    제목: 프로젝트 생성 기능 구현
    설명: 새 프로젝트를 생성하고 기본 설정을 저장
    우선순위: high
    예상시간: 6
    태그: 프로젝트, 백엔드
    [/CREATE_TASK]

    [CREATE_TASK]
    제목: 칸반 보드 UI 개선
    설명: 드래그 앤 드롭으로 태스크 상태 변경
    우선순위: medium
    예상시간: 8
    태그: UI, 칸반
    [/CREATE_TASK]
    ```

    3. **디자인팀 멘션**
    ```
    @디자인팀 위 기능들의 UI 디자인 작업을 부탁드립니다.
    와이어프레임과 Hi-fi 목업이 필요합니다.
    ```

    ---

    ## ⚡ 중요 규칙

    1. **문서는 자세하게**: 위키 문서는 다른 직원들도 읽을 수 있도록 상세하게 작성하세요
    2. **태스크는 구체적으로**: "무엇을" "왜" "어떻게" 할지 명확히 기술하세요
    3. **우선순위 판단**: 중요하고 긴급한 작업은 `high`, 일반적인 작업은 `medium`, 부가적인 작업은 `low`
    4. **협업 적극 활용**: 다른 부서의 도움이 필요하면 주저하지 말고 멘션하세요
    5. **기록 남기기**: 중요한 회의나 결정 사항은 협업 기록으로 남기세요

    이 도구들을 적극 활용하여 실제로 작업을 수행하세요!
    """
}
